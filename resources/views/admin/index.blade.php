                                lang=EN-US style='font-size:14.0pt'>)</span><span style='font-size:14.0pt;
                                font-family:宋体'>同桌成员可以远程点菜，别人发分享地址进入菜单，此地址有效期半小时（如果餐桌被清台在开台，分享菜单地址作废。也可以在</span><span
                                lang=EN-US style='font-size:14.0pt'>APP</span><span style='font-size:14.0pt;
