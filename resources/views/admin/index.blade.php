                                <p class=MsoNormal><b><span style='font-size:14.0pt;font-family:宋体'>外卖：</span></b><span
                                style='font-size:14.0pt;font-family:宋体'>用户通过无界优品用户端</span><span lang=EN-US
                                style='font-size:14.0pt'>APP</span><span style='font-size:14.0pt;font-family:
                                宋体'>或者微信公众号选择菜品下单，并使用支付宝支付、微信支付、余额支付方式进行支付，商家收到用户订单之后进行菜品制作，制作完成之后交给蜂鸟，达达等三方配送平台进行配送，也可以选择自行配送。</span></p>

                                <p class=MsoNormal><b><span style='font-size:14.0pt;font-family:宋体'>堂食点餐：</span></b><span
                                style='font-size:14.0pt;font-family:宋体'>用户到商家门店之后由商家服务员进行开台，用户或者是服务员均可以进行点餐，点餐完成之后提交订单，商家制作菜品并上菜。如遇到需要排号的情况，用户可以先取号，由服务员叫号。开台进行消费，开好之后的流程和正常消费流程一样。</span></p>

                                <p class=MsoNormal><b><span style='font-size:14.0pt;font-family:宋体'>二、细节</span></b></p>

                                <p class=MsoNormal><span lang=EN-US style='font-size:14.0pt'>1.</span><span
                                style='font-size:14.0pt;font-family:宋体'>每一个服务员都有一个二维码，用户可以通过</span><span
                                lang=EN-US style='font-size:14.0pt'>APP</span><span style='font-size:14.0pt;
                                font-family:宋体'>扫描服务员的二维码进行打赏，每一个菜品的小票上也都有厨师的打赏二维码，用户也可以扫描该二维码打赏厨师。</span></p>

                                <p class=MsoNormal><span lang=EN-US style='font-size:14.0pt'>2.</span><span
                                style='font-size:14.0pt;font-family:宋体'>根据就餐人数进行分配桌子，桌子类型有</span><span
                                lang=EN-US style='font-size:14.0pt'>2</span><span style='font-size:14.0pt;
                                font-family:宋体'>人，</span><span lang=EN-US style='font-size:14.0pt'>4</span><span
                                style='font-size:14.0pt;font-family:宋体'>人桌，</span><span lang=EN-US
                                style='font-size:14.0pt'>8</span><span style='font-size:14.0pt;font-family:
                                宋体'>人桌等。如果用户取号的时候输入</span><span lang=EN-US style='font-size:14.0pt'>3</span><span
                                style='font-size:14.0pt;font-family:宋体'>个人，那么会分配</span><span lang=EN-US
                                style='font-size:14.0pt'>4</span><span style='font-size:14.0pt;font-family:
                                宋体'>人桌。并且桌子类型最初会预先定义好，固定只有哪几种桌子类型，假如只有</span><span lang=EN-US style='font-size:
                                14.0pt'>2</span><span style='font-size:14.0pt;font-family:宋体'>人桌和</span><span
                                lang=EN-US style='font-size:14.0pt'>4</span><span style='font-size:14.0pt;
                                font-family:宋体'>人桌，那么</span><span lang=EN-US style='font-size:14.0pt'>2</span><span
                                style='font-size:14.0pt;font-family:宋体'>人桌的编号用</span><span lang=EN-US
                                style='font-size:14.0pt'>A00001</span><span style='font-size:14.0pt;font-family:
                                宋体'>开始，四人桌的编号从</span><span lang=EN-US style='font-size:14.0pt'>B00001</span><span
                                style='font-size:14.0pt;font-family:宋体'>开始，每天开始的第一单编号都重新刷新。</span></p>

                                <p class=MsoNormal><span lang=EN-US style='font-size:14.0pt'>3.</span><span
                                style='font-size:14.0pt;font-family:宋体'>服务员在排号操作时有三个功能，即</span><span
                                style='font-size:14.0pt'> </span><span style='font-size:14.0pt;font-family:
                                宋体'>叫号，入号，过号。叫号就是呼叫该哪一个号的用户用餐了，文字转换成语音的方式（集成讯飞</span><span lang=EN-US
                                style='font-size:14.0pt'>SDK</span><span style='font-size:14.0pt;font-family:
                                宋体'>，打包到本地，尽量不要请求接口）；入号就是安排用户到相应的桌子用餐，需要输入桌子编号；过号就是忽略这个号。</span></p>

                                <p class=MsoNormal><span lang=EN-US style='font-size:14.0pt'>4.</span><span
                                style='font-size:14.0pt;font-family:宋体'>取号必须登录。</span></p>

                                <p class=MsoNormal><span lang=EN-US style='font-size:14.0pt'>5.</span><span
                                style='font-size:14.0pt;font-family:宋体'>取号时，如果需就餐人数，大于单桌最大就餐人数（比如某一个商家单桌最大容纳人数为</span><span
                                lang=EN-US style='font-size:14.0pt'>8</span><span style='font-size:14.0pt;
